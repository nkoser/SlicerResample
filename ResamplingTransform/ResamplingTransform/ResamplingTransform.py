class ResamplingTransformLogic(ScriptedLoadableModuleLogic):
    """This class should implement all the actual
    computation done by your module.  The interface
    should be such that other python code can import
    this class and make use of the functionality without
    requiring an instance of the Widget.
    Uses ScriptedLoadableModuleLogic base class, available at:
    https://github.com/Slicer/Slicer/blob/main/Base/Python/slicer/ScriptedLoadableModule.py
    """

    def __init__(self) -> None:
        """
        Called when the logic class is instantiated. Can be used for initializing member variables.
        """
        ScriptedLoadableModuleLogic.__init__(self)

    def getParameterNode(self):
        return ResamplingTransformParameterNode(super().getParameterNode())

    def process(self,
                inputVolume: vtkMRMLScalarVolumeNode,
                outputVolume: vtkMRMLScalarVolumeNode,
                transform_: vtkMRMLTransformNode,
                targetVolume: vtkMRMLScalarVolumeNode,
                showResult: bool = True) -> None:
        """
        Run the processing algorithm.
        Can be used without GUI widget.
        :param inputVolume: volume to be thresholded
        :param outputVolume: thresholding result
        :param showResult: show output volume in slice viewers
        """

        if not inputVolume or not outputVolume or not targetVolume:
            raise ValueError("Input or output volume is invalid")

        import time
        startTime = time.time()
        logging.info('Processing started')

        inputImage = sitk.GetImageFromArray(slicer.util.arrayFromVolume(inputVolume))

        # Get the transform matrix from the vtkMRMLTransformNode and convert it to a SimpleITK transform
        #transformMatrix = transform_.GetMatrixTransformToParent()
        #transformMatrix= slicer.util.arrayFromTransformMatrix(transform_)# 4x4 matrix
        # Get the transform matrix from the vtkMRMLTransformNode
        transformMatrix = transform_.GetMatrixTransformToParent()  # 4x4 matrix

        # Convert vtkMatrix4x4 to a SimpleITK-compatible list
        transformList = [transformMatrix.GetElement(i, j) for i in range(4) for j in range(4)]

        # Create an AffineTransform with the matrix for 3D images
        sitkTransform = sitk.AffineTransform(3)
        sitkTransform.SetMatrix(transformList[:9])  # 3x3 matrix for rotation and scaling
        sitkTransform.SetTranslation(transformList[9:12])  # translation (offset)

        # Perform transformation and resampling using SimpleITK
        targetSpacing = targetVolume.GetSpacing()
        targetSize = targetVolume.GetImageData().GetDimensions()
        targetDirection=targetVolume.GetImageData().GetDirection()

        # Resample the input image with the transformation and target spacing
        transformedResampledImage = sitk.Resample(inputImage,
                                                  size=targetSize,  # Target size
                                                  transform=sitkTransform,  # Apply the transformation
                                                  interpolator=sitk.sitkLinear,  # Linear interpolation
                                                  outputSpacing=targetSpacing,  # Target spacing
                                                  outputOrigin=targetVolume.GetOrigin(),
                                                  outputDirection=targetDirection,
                                                  defaultValue=0)  # Background value

        # Convert the transformed and resampled image back to a numpy array
        resampledArray = sitk.GetArrayFromImage(transformedResampledImage)

        # Store the result in the output volume
        slicer.util.updateVolumeFromArray(outputVolume, resampledArray)

        # Optionally, show the result in the slice viewers
        if showResult:
            slicer.app.processEvents()

        # End timer
        stopTime = time.time()
        logging.info(f'Processing completed in {stopTime - startTime:.2f} seconds')

